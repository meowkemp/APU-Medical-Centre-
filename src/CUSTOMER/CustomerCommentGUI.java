    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        CommentService svc = new CommentService();

        int selectedRow = jtAppointments.getSelectedRow();
        String appointmentId = (selectedRow == -1) ? null : jtAppointments.getValueAt(selectedRow, 0).toString();

        String role = String.valueOf(cbRole.getSelectedItem()); // "Staff" or "Doctor"
        String text = txtComment.getText().trim();

        if (text.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a comment before submitting.");
            return;
        }
        if (appointmentId == null || appointmentId.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please select an appointment to comment on.");
            return;
        }

        // Build timestamp from Month/Day
        int month = (Integer) spnMonth.getValue();
        int day   = (Integer) spnDay.getValue();
        java.time.LocalDateTime createdAt = java.time.LocalDateTime.of(
                java.time.LocalDate.now().getYear(),
                month,
                day,
                java.time.LocalTime.now().getHour(),
                java.time.LocalTime.now().getMinute()
        );

        String fromUserId = userManager.getCurrentUser().getId();
        Comment c = svc.createComment(fromUserId, role, appointmentId, text, createdAt);
        svc.saveComment(c);

        JOptionPane.showMessageDialog(this, "Your comment has been submitted successfully");
        txtComment.setText("");
    }//GEN-LAST:event_btnSubmitActionPerformed

