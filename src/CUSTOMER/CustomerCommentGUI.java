    private void setupDateSpinners(){
        
        SpinnerNumberModel monthModel = new SpinnerNumberModel(1, 1, 12, 1);
        spnMonth.setModel(monthModel);
        
        SpinnerNumberModel dateModel = new SpinnerNumberModel(1, 1, 31, 1);
        spnDay.setModel(dateModel);

        setCurrentDate(); 
        
        spnMonth.addChangeListener(e -> {
            updateMaxDaysForMonth();
            validateNotPastDate();
        });
        
        spnDay.addChangeListener(e -> {
            validateNotPastDate(); 
        });

    }
    
    private void updateMaxDaysForMonth(){
        int month = (Integer) spnMonth.getValue();
        int maxDays = getMaxDaysForMonth(month); // Use helper method
    
        SpinnerNumberModel dayModel = (SpinnerNumberModel) spnDay.getModel();
        int currentDay = (Integer) dayModel.getValue();
    
        if(currentDay > maxDays){
            dayModel.setValue(maxDays);
        }
    
        dayModel.setMaximum(maxDays);
    }

    private int getMaxDaysForMonth(int month) {
        return switch (month) {
            case 2 -> 29;
            case 4, 6, 9, 11 -> 30;
            default -> 31;
        };
    }
    
    private void setCurrentDate(){
        Calendar calendar = Calendar.getInstance();
        int currentMonth = calendar.get(Calendar.MONTH) + 1;
        int currentDay = calendar.get(Calendar.DAY_OF_MONTH);
        
        spnMonth.setValue(currentMonth);
        spnDay.setValue(currentDay);
    }
    
    private void validateNotPastDate(){

        Calendar today = Calendar.getInstance();
        int currentMonth = today.get(Calendar.MONTH) + 1;
        int currentDay = today.get(Calendar.DAY_OF_MONTH);

        int selectedMonth = (Integer) spnMonth.getValue();
        int selectedDay = (Integer) spnDay.getValue();

        if (selectedMonth < currentMonth ||(selectedMonth == currentMonth && selectedDay < currentDay)) {
            JOptionPane.showMessageDialog(
            null,
            "You cannot select a past date",
            "Invalid Date",
            JOptionPane.WARNING_MESSAGE
        );
        setCurrentDate();
        }
    }

