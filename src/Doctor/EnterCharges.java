    private void addSelectedMedicine() {
        String name = (String) jComboBox1.getSelectedItem();
        int qty = Integer.parseInt((String) jComboBox2.getSelectedItem());
        double price = PRICE.getOrDefault(name, 0.0);

        // merge if exists
        for (int r = 0; r < medicineModel.getRowCount(); r++) {
            if (name.equals(medicineModel.getValueAt(r, 0))) {
                int oldQty = ((Number) medicineModel.getValueAt(r, 1)).intValue();
                int newQty = oldQty + qty;
                medicineModel.setValueAt(newQty, r, 1);
                medicineModel.setValueAt(price, r, 2);
                medicineModel.setValueAt(price * newQty, r, 3);
                recalcMedicineFeeAndDescription();
                return;
            }
        }
        medicineModel.addRow(new Object[]{name, qty, price, price * qty});
        recalcMedicineFeeAndDescription();
    }

    private void removeSelectedMedicine() {
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            medicineModel.removeRow(row);
            recalcMedicineFeeAndDescription();
        }
    }

    private void recalcMedicineFeeAndDescription() {
        double total = 0.0;
        for (int r = 0; r < medicineModel.getRowCount(); r++) {
            total += ((Number) medicineModel.getValueAt(r, 3)).doubleValue();
        }
        jTextField3.setText(String.format("%.2f", total));  // medicine fee (read-only)
    }
    private String createMedicineSummary() {
        StringBuilder s = new StringBuilder();
        for (int r = 0; r < medicineModel.getRowCount(); r++) {
            if (s.length() > 0) s.append("; ");
            s.append(medicineModel.getValueAt(r, 0)) // Medicine name
              .append(" x")
              .append(medicineModel.getValueAt(r, 1)); // Qty
        }
        return s.toString();
    }
    


