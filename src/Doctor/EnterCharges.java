    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    String apptId     = jTextField1.getText().trim();
    String serviceTx  = jTextField2.getText().trim();
    String medicineTx = jTextField3.getText().trim();
    
    String description = createMedicineSummary();

    if (apptId.isEmpty() || serviceTx.isEmpty() || medicineTx.isEmpty() ) {
        JOptionPane.showMessageDialog(this, "Please enter Appointment ID, Service Charges, Medicine fee, and Description.");
        return;
    }

    double service, medicine;
    try {
        service  = Double.parseDouble(serviceTx);
        medicine = Double.parseDouble(medicineTx);
        if (service < 0 || medicine < 0) throw new NumberFormatException();
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Service Charges and Medicine fee must be valid non-negative numbers.");
        return;
    }

    double total = service + medicine;
    int ok = JOptionPane.showConfirmDialog(
            this,
            "Appointment: " + apptId +
            "\nService:  RM " + String.format("%.2f", service) +
            "\nMedicine: RM " + String.format("%.2f", medicine) +
            "\nTotal:    RM " + String.format("%.2f", total) +
            "\n\nSave this charge?",
            "Confirm total",
            JOptionPane.YES_NO_OPTION);
    if (ok != JOptionPane.YES_OPTION) return;


    Appointment appt = new Appointment() {
    @Override
    public boolean bookAppointment(){
        return false;
    }
    
    };
    appt.setAppointmentId(apptId);
    appt.setDoctorId(currentDoctor.getId()); // ensure it matches the current doctor

    try {
        // POLYMORPHISM: call via interface
        Billable biller = currentDoctor;
        Charge saved = biller.createCharge(appt, total, description);

        JOptionPane.showMessageDialog(this, "Charge saved: " + saved.getId());
        medicineModel.setRowCount(0);
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField2.requestFocus();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Failed to save charge:\n" + ex.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

